namespace Project1
{
    internal class CalculatorBase
    {
        public int GetAdd(int value1, int value2) => value1 + value2;
        public int GetSubtract(int value1, int value2) => value1 - value2;
        public int GetMultiply(int value1, int value2) => value1 * value2;
        public int GetDivide(int value1, int value2) => value1 / value2;
    }
